class incrementdecrement  
{ 
public static void main(String[] args)  
{        
int x = 10;        
int y = ++x;        
System.out.println("y value is: " + y);   
x = 10;        
y = x++;        
System.out.println("y value is: " + y);   
x = 10; 
y = --x; 
System.out.println("y value is: " + y); 
x = 10; 
y = x--; 
System.out.println("y value is: " + y); 
} 
} 